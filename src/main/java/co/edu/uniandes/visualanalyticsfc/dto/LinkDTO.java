package co.edu.uniandes.visualanalyticsfc.dto;

/**
 *
 * @author Deisy
 */
public class LinkDTO {
    
    private int source;
    private int target;
    private int value;

    public int getSource() {
        return source;
    }

    public void setSource(int source) {
        this.source = source;
    }

    public int getTarget() {
        return target;
    }

    public void setTarget(int target) {
        this.target = target;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }
    
    
}
